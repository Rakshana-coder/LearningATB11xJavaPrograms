package ex_09_Switch;

public class Lab075_Interview7 {
    public static void main(String[] args) {

        char code = 'Z';
        switch (code){
            default:
                System.out.println("Hellooooooo");
                break;
            case 'A':
                System.out.println("65");
                break;
            case 'B':
                System.out.println("66");
                break;
        }

    }
}
