package ex_01_Operators;

public class Lab021_Unary_Operators {

    public static void main(String[] args) {

        // Unary Operator
        int a = +50;
        int b = -110;
        int result = a+b;
        System.out.println(a);
        System.out.println(b);
        System.out.println(result);


        int d = -1;
        d = d+1;
        System.out.println(d);


    }
}
