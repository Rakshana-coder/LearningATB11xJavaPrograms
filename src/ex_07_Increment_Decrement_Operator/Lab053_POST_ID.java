package ex_07_Increment_Decrement_Operator;

public class Lab053_POST_ID {
    public static void main(String[] args) {

        //Post Increment - Assign the value and then Increment.
        int a = 100;
        System.out.println(a++); //Output is 100  - Here it will just assign the same value
        System.out.println(a);   //Output is 101  - Increment the value
    }
}
