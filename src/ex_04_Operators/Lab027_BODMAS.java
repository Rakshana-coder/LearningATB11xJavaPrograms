package ex_04_Operators;

public class Lab027_BODMAS {
    public static void main(String[] args) {

        System.out.println((10 * 2 / 9 + 1) * 5);

        // 10 * 2 -> 20
        // 20/9 -> 2.22
        // 2.22+1 -> 3.222
        // 3.222 * 5 -> 16.11

        System.out.println((9 * 3 / 9 + 1) * 3);
    }
}
